void solve() {
    int n, m, k;
    cin >> n >> m >> k;
    int mx = min(n / k, m);
    m -= mx;
    trace(m);
    int mn = 1 + (m - 1) / (k - 1);
    if (m == 0) mn = 0;
    cout << mx - mn << '\n';
}
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t = 1;
    cin >> t;
    while (t--) solve();
}
